
public class Main {
    public static void main(String[] args) {
        // 1 ~ 100까지 소수 찾기
        int cnt = 0; // 이 변수가 '플래그 변수' 역할을 합니다.
        // 현재 숫자가 소수가 아닌 경우 1로 변경됩니다. (0이면 소수)

        // 외부 반복문: 2부터 100까지의 모든 숫자(i)를 검사합니다.
        // 소수는 1보다 커야 하므로 2부터 시작합니다.
        for(int i = 2; i <= 100; i++){
            // 내부 반복문: 현재 숫자 'i'가 소수인지 판별하기 위해 2부터 'i-1'까지의 숫자(j)로 나누어 봅니다.
            for(int j = 2; j < i; j++) {

                // 만약 'i'가 'j'로 나누어 떨어진다면 (나머지가 0이라면),
                // 'i'는 'j'를 약수로 가지므로 소수가 아닙니다.
                if(i % j == 0){
                    cnt++;     // 플래그 변수 'cnt'를 1로 증가시켜 '소수가 아니다'라고 표시합니다.
                    break;     // 약수를 하나 찾았으니, 더 이상 나눌 필요가 없습니다. (소수가 아님이 판명됨)
                    // 내부 반복문을 즉시 종료하고 다음 'i'로 넘어갑니다.
                }
            } // 내부 for문의 끝

            // 내부 반복문이 끝난 후 'cnt' 값이 여전히 0이라면,
            // 2부터 'i-1'까지 어떤 숫자로도 나누어 떨어지지 않았다는 의미입니다.
            // 즉, 'i'는 1과 자기 자신 외에 약수가 없는 소수입니다.
            if(cnt == 0) {
                System.out.print(i + "\t"); // 소수인 'i'를 출력하고 탭으로 간격을 둡니다.
            }
            cnt = 0; // 다음 숫자 'i'를 검사하기 위해 플래그 변수 'cnt'를 0으로 초기화합니다.
            // (다시 소수라고 가정하고 시작)
        }
    }
}